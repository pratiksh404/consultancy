    <h4>IELTS (International English Language Testing System)</h4>
    <p>It is a test of English Language proficiency. It is jointly managed by the University of Cambridge ESOL
        Examinations, the British Council and IDP.</p>
    <p><b>There are two versions of the IELTS:</b> The Academic Version and the General Version.</p>
    <h3>The IELTS Test Structure (Total Test Duration 2 hours 45 minutes.)</h3>
    <p>
        The IELTS test comprises of four Modules – Listening, Reading, Writing and Speaking. Scores are reported in
        “bands” for the individual modules and the overall tests. All candidates take the same Listening and Speaking
        Modules, while the Reading and Writing Modules differ depending on whether the candidate is taking the Academic
        or General Training Versions of the Test. .
    </p>
    <p>

        The first three modules – Listening, Reading and Writing (always in that order) – are completed in one day with
        no break in between. The Speaking Module may be taken, at the discretion of the test center. It is generally
        administered on the day of the test or on the following day.

        An IELTS result or Test Report Form (TRF) is valid for <b>two years</b>
    </p>
