    <h4>PTE (Pearson Test of English)</h4>
    <p>
        PTE Academic is accepted by universities in the USA, UK, Australia, amongst others.

        It was created by Person Language Tests part of the Pearson PLC group and endorsed by GMAC ® (Graduate
        Management Admission Council), owners of GMAT ® (Graduate Management Admission Test).

        It is a computer-based exam that focuses on real-life English used in academic surroundings. Throughout the
        test, students will listen to a variety of accents and academic language encountered at higher education
        institutions in English speaking countries
    </p>
